export class Calculator {
  public add(x: number, y: number) : number {
    return x + y;
  }
  public subtract(x: number, y: number) : number {
    return x - y;
  }
  public divide(x: number, y: number) : number {
    return x / y;
  }
  public mul(x: number, y: number) : number {
    return x * y;
  }
  public hello() {
    console.log("Hello");
  }
};
